//! `ipak` クレートは、パッケージ管理機能を提供するライブラリです。
//! パッケージのインストール、依存関係管理、システム情報の取得、およびプロジェクト設定の操作をサポートします。
//!
//! このクレートは以下の主要なモジュールで構成されています：
//! - `messages`: エラーメッセージやユーザ向けメッセージの管理
//! - `pkg`: パッケージメタデータとインストール操作の処理
//! - `project`: プロジェクト設定と構成の管理
//! - `system`: システム情報と環境設定の取得
//! - `utils`: ユーティリティ関数と補助機能
//! - `version`: バージョニングとバージョン範囲の管理

/// エラーメッセージやユーザ向けメッセージを管理します。
pub mod messages;

/// パッケージメタデータ、依存関係、インストール操作を処理します。
pub mod pkg;

/// プロジェクトの設定や構成を管理します。
pub mod project;

/// システム情報や環境設定を取得します。
pub mod system;

/// 一般的なユーティリティ関数や補助機能を提供します。
pub mod utils;

/// バージョニングおよびバージョン範囲の管理を処理します。
pub mod version;
